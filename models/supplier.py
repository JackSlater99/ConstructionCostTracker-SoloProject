class Supplier():
    def __init__(self, supplier_name, supplier_number, supplier_manager, supplier_address, supplier_phone, id=None):
        self.supplier_name = supplier_name
        self.supplier_number = supplier_number
        self.supplier_manager = supplier_manager
        self.supplier_address = supplier_address
        self.supplier_phone = supplier_phone
        self.id = id
